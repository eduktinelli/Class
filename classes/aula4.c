#include <stdio.h>

int main()
{
    int numero = 2;

    if (numero == 10)
    {
        printf("Numero vale 10\n");
    }
    else if (numero >= 3)
    {
        printf("Numero 11 ou mais\n");
    }
     else if (numero != 3)
    {
        printf("Numero nao e 3\n");
    }

    else 
    {
        printf("Numero nao vale mais que 11");
    }
    
    return 0; 
}